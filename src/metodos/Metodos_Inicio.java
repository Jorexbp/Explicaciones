package metodos;

public class Metodos_Inicio {
	
	public static void felizCumpleaños() {
		System.out.println("Te deseamos cumpleaños feliz");
		System.out.println("Por ser un chico excelente");
		System.out.println("Por ser un chico excelenteeeeee");
		System.out.println("Y siempre lo será");
	}

	public static void main(String[] args) {
		// ¿Qué son y para que sirven los métodos?

		
		/*
		 * Los métodos son partes de código que podemos almacenar para volver a ejecutar
		 * esa misma parte y así evitar reescribir el mismo código.
		 * 
		 */
		felizCumpleaños();
		felizCumpleaños();
		felizCumpleaños();

	}
}
